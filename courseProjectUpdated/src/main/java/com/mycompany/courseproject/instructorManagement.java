    private void jPanel5MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel5MousePressed
    // الحصول على الصف والعمود المختار
    int selectedRow = Table1.getSelectedRow();
    int selectedColumn = Table1.getSelectedColumn();

    // التأكد من أن الصف والعمود تم اختيارهما
    if (selectedRow != -1 && selectedColumn != -1) {
        // الحصول على القيمة الجديدة المدخلة من الحقل
        String newValue = TField1.getText().trim();

        // التأكد من أن القيمة ليست فارغة
        if (newValue.isEmpty()) {
            System.out.println("Please enter a valid value.");
            return;
        }

        // تحديث القيمة في الجدول
        DefaultTableModel tableModel = (DefaultTableModel) Table1.getModel();
        tableModel.setValueAt(newValue, selectedRow, selectedColumn);

        // إنشاء قائمة من المعلمين المحدثين
        int rowCount = tableModel.getRowCount();
        List<Instructor> updatedInstructors = new ArrayList<>();

        for (int i = 0; i < rowCount; i++) {
            String name = tableModel.getValueAt(i, 0).toString();
            String course = tableModel.getValueAt(i, 1).toString();
            int id = Integer.parseInt(tableModel.getValueAt(i, 2).toString());
            updatedInstructors.add(new Instructor(name, course, id));
        }

        // كتابة البيانات المحدثة إلى الملف
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(Instructor.FILE_NAME.toFile()))) {
            for (Instructor instructor : updatedInstructors) {
                bw.write(instructor.getName() + "-" + instructor.getCourse() + ", " + instructor.getId());
                bw.newLine();
            }
            System.out.println("Data updated successfully.");
        } catch (IOException e) {
            System.out.println("An error occurred while updating the data.");
            e.printStackTrace();
        }
    } else {
        System.out.println("No row or column selected.");
    }


    }//GEN-LAST:event_jPanel5MousePressed

