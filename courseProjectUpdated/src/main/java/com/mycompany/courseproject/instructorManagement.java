Table1.getSelectionModel().addListSelectionListener(new javax.swing.event.ListSelectionListener() {
    public void valueChanged(javax.swing.event.ListSelectionEvent evt) {
        if (!evt.getValueIsAdjusting() && Table1.getSelectedRow() != -1) {
            int selectedRow = Table1.getSelectedRow();
            int selectedColumn = Table1.getSelectedColumn();
            
            // إذا كان المستخدم يحاول تعديل العمود الخاص بالـ ID (العمود الثالث)
            if (selectedColumn == 2) {  // assuming the ID is in the third column (index 2)
                System.out.println("You cannot edit the ID.");
                TField1.setText("");  // مسح الحقل النصي
                return;  // منع التغيير في الـ ID
            }

            // الحصول على القيمة المحددة في العمود المختار
            String selectedValue = tableModel.getValueAt(selectedRow, selectedColumn).toString();

            // ملء الحقل النصي بالقيمة المحددة بناءً على العمود
            TField1.setText(selectedValue);
        }
    }
});

