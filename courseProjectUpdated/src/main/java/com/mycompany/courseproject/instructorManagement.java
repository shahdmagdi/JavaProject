    private void jPanel6MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel6MousePressed
    // الحصول على الصف المختار من الجدول
    int selectedRow = Table1.getSelectedRow();

    // التأكد من أن هناك صف مختار
    if (selectedRow != -1) {
        // إزالة الصف من نموذج الجدول
        DefaultTableModel tableModel = (DefaultTableModel) Table1.getModel();
        tableModel.removeRow(selectedRow);

        // تحديث الملف النصي
        // قراءة كل البيانات الحالية من الجدول
        int rowCount = tableModel.getRowCount();
        List<Instructor> updatedInstructors = new ArrayList<>();

        // جمع البيانات المحدثة من الجدول
        for (int i = 0; i < rowCount; i++) {
            String name = tableModel.getValueAt(i, 0).toString();
            String course = tableModel.getValueAt(i, 1).toString();
            int id = Integer.parseInt(tableModel.getValueAt(i, 2).toString());
            updatedInstructors.add(new Instructor(name, course, id));
        }

        // كتابة البيانات المحدثة إلى الملف النصي
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(Instructor.FILE_NAME.toFile()))) {
            // كتابة جميع البيانات بعد التعديل إلى الملف
            for (Instructor instructor : updatedInstructors) {
                bw.write(instructor.getName() + "-" + instructor.getCourse() + ", " + instructor.getId());
                bw.newLine();
            }
            System.out.println("Selected row deleted successfully.");
        } catch (IOException e) {
            // في حالة حدوث استثناء أثناء الكتابة إلى الملف
            System.out.println("An error occurred while updating the data.");
            e.printStackTrace();
        }

        // مسح البيانات في الحقل النصي TField1
        TField1.setText("");
    } else {
        // في حال لم يتم اختيار أي صف
        System.out.println("No row selected.");
    }


    }//GEN-LAST:event_jPanel6MousePressed

