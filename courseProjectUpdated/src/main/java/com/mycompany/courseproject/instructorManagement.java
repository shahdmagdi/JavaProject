    public void updateInstructorTable() {
        // الحصول على النموذج الحالي للجدول
        DefaultTableModel tableModel = (DefaultTableModel) table1.getModel();
        tableModel.setRowCount(0);

        // قراءة البيانات المحدثة من الملف
        List<Instructor> updatedInstructors = Instructor.readFromFile();

        // إضافة البيانات المحدثة إلى الجدول
        for (Instructor instructor : updatedInstructors) {
            tableModel.addRow(new Object[]{instructor.getId(), instructor.getName(), instructor.getCourse()});
        }

        // إعادة رسم الجدول
        table1.revalidate();
        table1.repaint();
    }

